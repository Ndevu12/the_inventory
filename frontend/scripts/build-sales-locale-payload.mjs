function deepMerge(a, b) {
  if (b === undefined) return a
  if (b === null || typeof b !== "object" || Array.isArray(b)) return b
  const o = { ...a }
  for (const k of Object.keys(b)) {
    const bv = b[k]
    const av = o[k]
    if (
      bv &&
      typeof bv === "object" &&
      !Array.isArray(bv) &&
      av &&
      typeof av === "object" &&
      !Array.isArray(av)
    ) {
      o[k] = deepMerge(av, bv)
    } else {
      o[k] = bv
    }
  }
  return o
}

